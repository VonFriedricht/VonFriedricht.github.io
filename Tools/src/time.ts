let ms: number = 1

let s: number = 1000 * ms
let sec: number = s

let min: number = 60 * s
let minute: number = min

let h: number = 60 * min
let hour: number = h

let day: number = 24 * h
let week: number = 7 * day
let month: number = 30 * day
let year: number = 12 * month

export { ms, s, sec, min, minute, h, hour, day, week, month, year }
