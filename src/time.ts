export let ms: number = 1;

export let s: number = 1000 * ms;
export let sec: number = s;

export let min: number = 60 * s;
export let minute: number = min;

export let h: number = 60 * min;
export let hour: number = h;

export let day: number = 24 * h;
export let week: number = 7 * day;
export let month: number = 30 * day;
export let year: number = 12 * month;
